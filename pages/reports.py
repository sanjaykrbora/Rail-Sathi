    with pd.ExcelWriter(
        excel_buffer,
        engine="openpyxl"
    ) as writer:

        filtered_data.to_excel(
            writer,
            index=False,
            sheet_name=report_type
        )

    st.download_button(
        label="📊 Download Excel Report",
        data=excel_buffer.getvalue(),
        file_name=f"{report_type.lower().replace(' ', '_')}_report.xlsx",
        mime="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
    )

    st.divider()

    st.subheader("ℹ️ Report Information")

    info1, info2 = st.columns(2)

    with info1:

        st.info(
            f"""
            **Selected Report:** {report_type}

            **Export Format:** {export_type}

            **Filtered Records:** {len(filtered_data)}
            """
        )

    with info2:

        st.success(
            f"""
            **Database:** railway.db

            **Generated By:** Rail Sathi

            **Status:** Ready for Export
            """
        )

    st.divider()
    st.subheader("📊 Report Analytics")

    analytics_col1, analytics_col2 = st.columns(2)

    with analytics_col1:

        record_chart = pd.DataFrame(
            {
                "Category": [
                    "Coaches",
                    "Employees",
                    "Machines",
                    "Maintenance",
                    "Workshop Shops"
                ],
                "Records": [
                    len(coaches),
                    len(employees),
                    len(machines),
                    len(maintenance),
                    len(shops)
                ]
            }
        )

