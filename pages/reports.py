import sqlite3
from pathlib import Path

import pandas as pd
import streamlit as st


BASE_DIR = Path(__file__).resolve().parent.parent
DB_PATH = BASE_DIR / "database" / "railway.db"


def get_connection():
    return sqlite3.connect(DB_PATH)


def load_css():
    css_path = BASE_DIR / "css" / "style.css"

    if css_path.exists():
        with open(css_path, "r", encoding="utf-8") as file:
            st.markdown(
                f"<style>{file.read()}</style>",
                unsafe_allow_html=True
            )


def app():

    load_css()

    connection = get_connection()

    coaches = pd.read_sql_query(
        "SELECT * FROM coaches",
        connection
    )

    employees = pd.read_sql_query(
        "SELECT * FROM employees",
        connection
    )

    machines = pd.read_sql_query(
        "SELECT * FROM machines",
        connection
    )

    maintenance = pd.read_sql_query(
        "SELECT * FROM maintenance",
        connection
    )

    shops = pd.read_sql_query(
        "SELECT * FROM shops",
        connection
    )

    st.title("📄 Reports")

    st.caption(
        "Generate and export railway workshop reports."
    )

    st.divider()

    report_col1, report_col2 = st.columns(2)

    report_type = report_col1.selectbox(
        "Report Category",
        [
            "Coaches",
            "Employees",
            "Machines",
            "Maintenance",
            "Workshop Shops"
        ]
    )

    export_type = report_col2.selectbox(
        "Export Format",
        [
            "CSV",
            "Excel"
        ]
    )

    report_data = {
        "Coaches": coaches,
        "Employees": employees,
        "Machines": machines,
        "Maintenance": maintenance,
        "Workshop Shops": shops
    }

    selected_report = report_data[report_type]

    st.divider()

    total_records = len(selected_report)

    total_columns = len(selected_report.columns)

    last_updated = "Today"

    summary1, summary2, summary3 = st.columns(3)

    summary1.metric(
        "Records",
        total_records
    )

    summary2.metric(
        "Columns",
        total_columns
    )

    summary3.metric(
        "Updated",
        last_updated
    )

    st.divider()
    st.subheader("📋 Report Preview")

    search = st.text_input(
        "Search Record"
    )

    filtered_data = selected_report.copy()

    if search:

        mask = filtered_data.astype(str).apply(
            lambda column: column.str.contains(
                search,
                case=False,
                na=False
            )
        ).any(axis=1)

        filtered_data = filtered_data[mask]

    st.dataframe(
        filtered_data,
        use_container_width=True,
        hide_index=True
    )

    st.divider()

    st.subheader("📥 Export Report")

    csv = filtered_data.to_csv(
        index=False
    ).encode("utf-8")

    st.download_button(
        label="📄 Download CSV Report",
        data=csv,
        file_name=f"{report_type.lower().replace(' ', '_')}_report.csv",
        mime="text/csv"
    )

